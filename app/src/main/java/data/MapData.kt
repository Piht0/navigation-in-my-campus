    private val originalGrid: Array<BooleanArray> by lazy { decodeGrid() }

    var grid: Array<BooleanArray> = emptyArray()
        private set

    private var initialized = false

    private fun ensureInitialized() {
        if (!initialized) {
            grid = copyGrid(originalGrid)
            initialized = true
        }
    }

    fun isPassable(row: Int, col: Int): Boolean {
        ensureInitialized()
        if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return false
        return grid[row][col]
    }

    fun isInBounds(row: Int, col: Int): Boolean {
        return row in 0 until ROWS && col in 0 until COLS
    }

    fun setObstacle(row: Int, col: Int): Boolean {
        ensureInitialized()
        if (!isInBounds(row, col)) return false
        if (!grid[row][col]) return false 
        grid[row][col] = false
        return true
    }

    fun setPassage(row: Int, col: Int): Boolean {
        ensureInitialized()
        if (!isInBounds(row, col)) return false
        if (grid[row][col]) return false 
        grid[row][col] = true
        return true
    }

    fun resetGrid() {
        grid = copyGrid(originalGrid)
        initialized = true
    }

    fun isModified(row: Int, col: Int): Boolean {
        ensureInitialized()
        if (!isInBounds(row, col)) return false
        return grid[row][col] != originalGrid[row][col]
    }

    fun getModifiedCount(): Int {
        ensureInitialized()
        var count = 0
        for (r in 0 until ROWS) {
            for (c in 0 until COLS) {
                if (grid[r][c] != originalGrid[r][c]) count++
            }
        }
        return count
    }

    private fun decodeGrid(): Array<BooleanArray> {
        val bytes = Base64.decode(ENCODED_DATA, Base64.DEFAULT)
        val result = Array(ROWS) { BooleanArray(COLS) }
        var bitIndex = 0
        for (r in 0 until ROWS) {
            for (c in 0 until COLS) {
                val byteIdx = bitIndex / 8
                val bitOffset = 7 - (bitIndex % 8)
                result[r][c] = ((bytes[byteIdx].toInt() shr bitOffset) and 1) == 1
                bitIndex++
            }
        }
        return result
    }

    private fun copyGrid(source: Array<BooleanArray>): Array<BooleanArray> {
        return Array(source.size) { r -> source[r].copyOf() }
    }
