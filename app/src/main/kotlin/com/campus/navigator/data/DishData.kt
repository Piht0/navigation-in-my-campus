package com.campus.navigator.data

/**
 * Модель блюда в глобальном справочнике [FoodRepository].
 *
 * Блюда хранятся независимо от заведений; связь «заведение ↔ блюдо»
 * реализована через таблицу ссылок [links] в репозитории (many-to-many).
 * Одно блюдо может быть привязано к нескольким заведениям.
 */
data class DishData(
    /** Уникальный идентификатор блюда, присваивается репозиторием при вставке. */
    val id: Long,
    /** Название блюда (например, «Борщ», «Пицца Маргарита»). */
    var name: String
)
