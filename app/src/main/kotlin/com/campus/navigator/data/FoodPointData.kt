package com.campus.navigator.data

/**
 * Модель заведения питания для хранилища [FoodRepository].
 *
 * Отличается от [com.campus.navigator.FoodPoint] тем, что:
 *  - хранит уникальный числовой [id] (генерируется репозиторием);
 *  - имеет флаг [isOnMap] — показывает, отображается ли точка на карте прямо сейчас;
 *  - сохраняется на диск через SharedPreferences + JSON.
 *
 * [FoodPoint] — это лёгкое in-memory представление в MapData,
 * [FoodPointData] — полная запись в репозитории.
 */
data class FoodPointData(
    /** Уникальный идентификатор записи, присваивается репозиторием при вставке. */
    val id: Long,
    /** Строка (клетка) на карте, где расположено заведение. */
    val row: Int,
    /** Столбец (клетка) на карте, где расположено заведение. */
    val col: Int,
    /** Пользовательское название заведения (может быть пустым). */
    var name: String = "",
    /** Текстовый отзыв пользователя о заведении. */
    var review: String = "",
    /** Теги через запятую для быстрой фильтрации (например, «кафе, суши»). */
    var tags: String = "",
    /** true — точка отображается на карте; false — скрыта, но сохранена в БД. */
    var isOnMap: Boolean = true
)
